package cs414.as5.btsaunde.garagesystem.model;

/**
 * Represents a Sign at a Gate.
 * 
 * @author Bryan Saunders <btsaunde@gmail.com>
 * 
 */
public class Sign {

	/**
	 * Sign Text
	 */
	private String text;

	/**
	 * Get the text.
	 * 
	 * @return the text
	 */
	public String getText() {
		return this.text;
	}

	/**
	 * Set the text.
	 * 
	 * @param text
	 *            the text to set
	 */
	public void setText(String text) {
		this.text = text;
	}

}
